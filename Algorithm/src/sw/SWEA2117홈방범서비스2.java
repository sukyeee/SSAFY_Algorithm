package sw;

import java.io.BufferedReader;
import java.io.InputStreamReader;
import java.util.ArrayList;
import java.util.List;
import java.util.StringTokenizer;

public class SWEA2117홈방범서비스2 {

	static int T, N, M;
	static int map[][];
	static List<Dist> homes = new ArrayList<>();
	static int max;
	public static void main(String[] args) throws Exception {

		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));

		T = Integer.parseInt(br.readLine());

		for (int t = 1; t <= T; t++) {
			max = 0;
			homes.clear();
			
			StringTokenizer st = new StringTokenizer(br.readLine());

			N = Integer.parseInt(st.nextToken());
			M = Integer.parseInt(st.nextToken());
			map = new int[N][N];
			for (int i = 0; i < N; i++) {
				st = new StringTokenizer(br.readLine());
				for (int j = 0; j < N; j++) {
					map[i][j] = Integer.parseInt(st.nextToken());
					if(map[i][j] == 1) homes.add(new Dist(i, j)); // 집 위치 저장 
				}
			}
			
			for (int k = N+1; k >= 0; k--) {
				int K = k * k + (k - 1) * (k - 1);
				
				
				for (int i = 0; i < N; i++) {
					for (int j = 0; j < N; j++) {
				
						int cnt = 0;
						// 좌표와 집 사이 거리 차 구하기 
						for (Dist home : homes) {
							
							int d = getDistance(i, j, home.y, home.x);
							if( d <= k-1 ) {
								cnt++;
							}
							
						}
						// 집의 개수*수익 - 운영비용 = 보안회사의 이익 (>=0이면 손해를 보지 않음)    
						if( cnt*M - K >= 0 ) {
							// 최대 집 개수 갱신
							max = Math.max(max, cnt);
						}
					}
				}
				
			}

			
			System.out.println("#" + t + " " + max);
			
		} // testcase
		
	}
	
	static int getDistance(int y1, int x1, int y2, int x2) {
		return Math.abs(y1 - y2) + Math.abs(x1 - x2);
	}
	
	static class Dist {
		int y, x;

		public Dist(int y, int x) {
			super();
			this.y = y;
			this.x = x;
		}
	}
}

