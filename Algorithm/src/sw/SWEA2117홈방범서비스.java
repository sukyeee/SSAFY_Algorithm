package sw;

import java.io.BufferedReader;
import java.io.InputStreamReader;
import java.util.ArrayList;
import java.util.List;
import java.util.StringTokenizer;

public class SWEA2117홈방범서비스 {

	static int T, N, M;
	static int map[][];
	static List<Dist> list = new ArrayList<>();
	static int max;
	static int house;
	public static void main(String[] args) throws Exception {

		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));

		T = Integer.parseInt(br.readLine());

		for (int t = 1; t <= T; t++) {
			max = 0;
			list.clear();
			StringTokenizer st = new StringTokenizer(br.readLine());

			N = Integer.parseInt(st.nextToken());
			M = Integer.parseInt(st.nextToken());
			map = new int[N][N];
			for (int i = 0; i < N; i++) {
				st = new StringTokenizer(br.readLine());
				for (int j = 0; j < N; j++) {
					map[i][j] = Integer.parseInt(st.nextToken());
					if (map[i][j] == 1)
						list.add(new Dist(i, j));
				}
			}
			// 입력 완

			// K가 커질 수록 운영 비용이 커진다.
			// 운영 비용은 서비스 영역의 면적과 동일 ( K*K + (K-1) * (K-1) )
			// 최대한 많은 집에 홈방범서비스 제공!

			// 하나의 집이 지불할 수 있는 비용은 M

			// 손해를 보지 않고 서비스 가능한 최대 집의 수 구하기

			// 홈방범 서비를 가장 많은 집들에 제공하는 서비스 영역 찾기
			// 그 때 홈방범 서비스를 제공 받는 집들의 수를 출력

			// 두 점 사이의 거리를 통해 비교

			// n+1
			// 거리가 k 미만일 때 집의 개수 카운트

			// 현재 집에서서부터 k 만큼 떨어진 위치 안의 집 개수구하기 
			
			for (int k = N+1; k > 0; k--) {

				
				int cost = k * k + (k - 1) * (k - 1);
//				if ( max >= cost)break;
				for (int i = 0; i < N; i++) {
					for (int j = 0; j < N; j++) {
						 house = 0;
						for (Dist e : list) {
							int dist = getDistance(i, j, e.y, e.x); // 현재 위치와, 구하려는 곳의 거리 차이
								// 집이 있다면 cnt++
								
								if (dist <= k-1 )  // N 
									house++;
							
						}
						// 집 개수 * m >= cost 일 때만 .
						if (house * M  - cost >= 0) {
							max = Math.max(max, house); // 최댓값 갱신
						}
					}
				
				}

			}

			System.out.println("#" + t + " " + max);
		} // testcase

	}

	static int getDistance(int y1, int x1, int y2, int x2) {
		return Math.abs(y1 - y2) + Math.abs(x1 - x2);
	}

	static class Dist {
		int y, x;

		public Dist(int y, int x) {
			super();
			this.y = y;
			this.x = x;
		}

	}
}

